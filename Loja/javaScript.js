function scrollToSection(sectionId) {
    const section = document.getElementById(sectionId);
    section.scrollIntoView({ behavior: 'smooth' }); // Transição suave
}

function troca1(){
    let favorito = document.getElementById("coracao1")
    if (favorito.src.includes("img/coracaoPreenchido.png")){
        favorito.src = 'img/coracao.png'
    } else{
        favorito.src = 'img/coracaoPreenchido.png'
    }
} 
